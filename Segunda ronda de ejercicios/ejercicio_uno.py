"""
Dados ​ dos valores enteros A y B, informar la suma, la resta y el producto.
"""

print("Ingrese dos valores a continuación.")

a = int(input("Primer valor:  "))
b = int(input("Segundo valor:  "))

suma = a + b
resta = a - b
producto  = a * b

print(f"La suma: {suma}")
print(f"La resta: {resta}")
print(f"El producto: {producto}")

